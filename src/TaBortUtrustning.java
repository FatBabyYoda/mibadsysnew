    private void fyllComboBoxUtrustning() {

        try {

            ArrayList<HashMap<String, String>> utrustningsLista = Start.idb.fetchRows("SELECT Utrustnings_ID, Benamning FROM utrustning");
            jComboBox1.removeAllItems();
            for (int i = 0; i < utrustningsLista.size(); i++) {
                
                String utrustningsId = utrustningsLista.get(i).get("Utrustnings_ID");
                String benamning = utrustningsLista.get(i).get("Benamning");

                String utrustningCombo = utrustningsId + ": " + benamning;
                jComboBox1.addItem(utrustningCombo);
            }
        } catch (InfException e) {
            JOptionPane.showMessageDialog(null, "Något gick fel!");
        }
    }
    
    
