    private void btnTillbakaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTillbakaActionPerformed
        // TODO add your handling code here:

        try {

            //Hämtar valt objekt från combon
            String valdUtrustningObjektet = (String) jComboBox1.getSelectedItem();

            //Checkar om nåt objekt är valt eller inte
            if (valdUtrustningObjektet != null) {
                // Extrahera Utrustnings_ID från den valda strängen

                //Splittar strängen vid ":" så id blir ensammare än mig. Det gör att det returnerar
                //en array med 2 delar, ID o Benamning. [0] tar första delen (ID). 
                //Trim tar bort mellanslag runt ID (Det var bra om man gjorde det ok?)
                //Så valdUtrustningObjektet är t.ex. 99: SuperblastMegaSonic som kommer ge
                //utrustningsID 99. Frågan byggs sedan på bara Id:t för o ta bort valda raden.
                //Integer.parseInt konverterar en sträng till int
                int utrustningsId = Integer.parseInt(valdUtrustningObjektet.split(":")[0].trim());

                String fraga = "DELETE FROM utrustning WHERE Utrustnings_ID = " + utrustningsId;

                try {
                    
                    //Delete metoden anropas från Start.idb för o ta bort raden
                    //fraga = (fraga där uppe)
                    Start.idb.delete(fraga);
                    JOptionPane.showMessageDialog(this, "Utrustningen har tagits bort.", "Meddelande", JOptionPane.INFORMATION_MESSAGE);
                    //Uppdaterar combon efter man tryckt verkställ
                    fyllComboBoxUtrustning();
                    
                    //Detta behövs för o fånga upp sql fel  
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Ett fel uppstod vid borttagning av utrustning.", "Fel", JOptionPane.ERROR_MESSAGE);
                }
            }
            
            //Denna behövdes för att första try skulle få nåt catch. Headless.. fick jag som förfrågan om man ville ha så ja ba tog de
        } catch (HeadlessException | NumberFormatException e) { 
            // För att skriva ut felet till konsolen (går att ta bort om du inte behöver det)
            JOptionPane.showMessageDialog(this, "Ett oväntat fel inträffade.", "Fel", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTillbakaActionPerformed

