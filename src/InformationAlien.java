    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // fyller "jTextArea" med all information om en specific alien
        infoLista.setText("");
        try {
            String valdAlien = Alternativen.getSelectedItem().toString();
            String alienNamn = Start.idb.fetchSingle("SELECT NAMN FROM ALIEN WHERE NAMN ='" + valdAlien + "'");
            ArrayList<String> namnLista = Start.idb.fetchColumn("SELECT NAMN FROM ALIEN WHERE NAMN ='" + alienNamn + "'");
            ArrayList<String> idLista = Start.idb.fetchColumn("SELECT ALIEN_ID FROM ALIEN WHERE NAMN ='" + alienNamn + "'");
            ArrayList<String> epostLista = Start.idb.fetchColumn("SELECT EPOST FROM ALIEN WHERE NAMN ='" + alienNamn + "'");
            ArrayList<String> registreringdatumLista = Start.idb.fetchColumn("SELECT REGISTRERINGSDATUM FROM ALIEN WHERE NAMN ='" + alienNamn + "'");
            ArrayList<String> telefonLista = Start.idb.fetchColumn("SELECT TELEFON FROM ALIEN WHERE NAMN ='" + alienNamn + "'");
            ArrayList<String> platsLista = Start.idb.fetchColumn("SELECT BENAMNING FROM OMRADE JOIN ALIEN ON OMRADES_ID = ALIEN.PLATS WHERE ALIEN.NAMN ='" + alienNamn + "'");
            ArrayList<String> ansvarigLista = Start.idb.fetchColumn("SELECT AGENT.NAMN FROM AGENT JOIN ALIEN ON AGENT_ID = ANSVARIG_AGENT WHERE ALIEN.NAMN ='" + alienNamn + "'");

            for (int i = 0; i < namnLista.size(); i++) {
                infoLista.append(" Namn: " + namnLista.get(i) + "\n" + " Id: " + idLista.get(i) + "\n" + " Epost: " + epostLista.get(i) + "\n" + " Registreringsdatum: " + registreringdatumLista.get(i) + "\n" + " Telefon: " + telefonLista.get(i) + "\n" + " Plats: " + platsLista.get(i) + "\n" + " Ansvarig: " + ansvarigLista.get(i) + "\n");
            }
        } catch (InfException e) {
            JOptionPane.showMessageDialog(null, "Något gick fel!");
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

