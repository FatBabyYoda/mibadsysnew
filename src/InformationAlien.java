    private void fyllIallaAliensIComboBox() {
        try {
            ArrayList<HashMap<String, String>> AlienIdLista = Start.idb.fetchRows("SELECT ALIEN_ID, Alien.Namn FROM ALIEN");
            for (int i = 0; i < AlienIdLista.size(); i++) {
                Alternativen.addItem(AlienIdLista.get(i).get("Namn"));
            }

        } catch (InfException e) {
            JOptionPane.showMessageDialog(null, "Något gick fel!");
        }
    }


